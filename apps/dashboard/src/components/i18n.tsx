const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Language>(defaultLanguage);

  useEffect(() => {
    const stored = window.localStorage.getItem(storageKey);
    if (stored && supportedLanguages.some(({ code }) => code === stored)) {
      const nextLang = stored as Language;
      setLangState(nextLang);
      document.documentElement.lang = nextLang;
    }
  }, []);

  const value = useMemo<I18nContextValue>(
    () => ({
      lang,
      setLang: (nextLang) => {
        const scrollY = window.scrollY;
        setLangState(nextLang);
        window.localStorage.setItem(storageKey, nextLang);
        document.documentElement.lang = nextLang;
        window.requestAnimationFrame(() => {
          window.scrollTo({ left: 0, top: scrollY });
          window.setTimeout(() => window.scrollTo({ left: 0, top: scrollY }), 0);
        });
      },
      t: (key) => translations[lang][key] ?? translations.uk[key] ?? key,
    }),
    [lang],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}

export function LanguageSwitcher({ compact = false }: { compact?: boolean }) {
  const { lang, setLang } = useI18n();

  return (
    <div className={compact ? "language-switcher compact" : "language-switcher"}>
      {supportedLanguages.map((option) => (
        <button className={lang === option.code ? "active" : ""} key={option.code} onClick={() => setLang(option.code)} type="button">
          {option.shortLabel}
        </button>
      ))}
    </div>
  );
}


